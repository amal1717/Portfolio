                        <h2 id="proposition">Value Proposition Canvas</h2>
                        <p>Value Proposition Canvas hjælper virksomheden med at skabe værdi for kunderne og handler om den midterste del af Business Model Canvas, altså værditilbuddet. Men for at virksomheden kan skabe værdi for kunderne kræver det at virksomheden kender målgruppen rigtig godt og derfor forudsætter modellen en veldefineret målgruppe. Value Proposition Canvas har to sider, hvor den ene side er kunden og den anden side er virksomheden (Salesboost, 2020).</p>
                        
                        <br>
                        
                        <img class="billeder" id="selfservice" src="Billeder/BMC/value_proposition_canvas.png" alt="Billede af Business Model Canvas">
                        <p>(Salesboost, 2020)</p>
                        
                        <h3>Kunden</h3>
                        <p>I forhold til kunderne skal virksomheden undersøge ”jobs”, ”pains” og ”gains”. ”Jobs” handler om hvad kunderne ønsker at få gjort ved hjælp af virksomhedens produkt eller ydelse. Det kan f.eks. være at kunderne gerne vil have en mobiltelefon til at ringe, sende beskeder, tage billeder osv. ”Pains” handler om hvad der frustrerer eller irriterer kunderne, altså hvad de gerne vil være fri for. Det kan f.eks. være dårlig batteritid, dårlig billedkvalitet, påtvunget abonnement osv. ”Gains” handler derimod om hvad de ønsker og gerne vil have, f.eks. et godt kamera, et lækkert design, brugervenlighed osv. Når virksomheden har fundet ud af hvad kundernes jobs, pains og gains er, kan virksomheden fokuserer på at målrettet deres side så den matcher kundernes (Salesboost, 2020) (Schmith, Business Model Canvas, 2020).</p>
                        
                        <h3>Virksomheden</h3>
                        <p>I forhold til virksomheden skal der ses på ”product og services”, ”pain relievers” og ”gain creators”. ”Product og services” er de produkter eller ydelser virksomheden tilbyder eller kan udvikle, som kan løse de ”jobs” som kunderne har, f.eks. en mobiltelefon. ”Pain relievers” handler om hvordan virksomhedens produkter eller ydelser kan afhjælpe de ”pains” som kundernes oplever, f.eks. en mobiltelefon med god batteritid og et godt kamera. Derimod handler ”Gains creators” om hvordan virksomhedens produkter er med til at skabe værdi for kunderne, f.eks. en lækkert design og brugervenlighed (Salesboost, 2020) (Schmith, Business Model Canvas, 2020). </p>
                        
                        <h3>Match</h3>
                        <p>Når kunderne finder et produkt eller en ydelse som klarer deres ”jobs”, fjerner deres ”pains” og lever op til deres ”gains” opstår der et match mellem kunderne og virksomheden. Hele essensen bag Value Propositions Canvas er stræben efter et match mellem kunder og virksomhed (Schmith, Business Model Canvas, 2020). </p>
                        
