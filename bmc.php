                            <h2>Business Model Canvas</h2>
                            <p>Business Model Canvas er et strategisk ledelsesværktøj som kan bruges til at opnå en bedre forståelse af en virksomhed. Det er en god ide at anvende modellen ved opstart af en ny virksomhed til at udvikle virksomhedens forretningsmodel, men den er også vigtig for eksisterende virksomheder at benytte til strategisk planlægning. Specielt hvis virksomheden aldrig før har benyttet sig af den. Modellen består af ni byggesten, som tilsammen udgør virksomhedens forretningsmodel. Når en virksomhed anvender modellen, er den både med til at visualisere de forskellige elementer som virksomheden er bygget op på samt belyse muligheder for forbedringer (Andersen, Jensen, Olsen, Olsen, & Schmalz, Kapitel 3, 2015). Helt konkret fastlægger Business Model Canvas hvem virksomhedens kunder er, hvilken værdi virksomheden giver kunderne og igennem hvilke kanaler virksomheden skaber relationen til kunderne. Derudover er der også fokus på hvilke aktiviteter, ressourcer og samarbejdspartnere der er behov for og hvilke indtægter og udgifter virksomheden har (Schmith, Business Model Canvas, 2020). 
                            </p>
                            
                    </section>
                
                    <section id="byggesten">
                        <h2>De ni byggesten</h2>
                            <h3>Kundesegmenter</h3>
                            <p>
                            -	De forskellige grupper af kunder som virksomheden stræber efter at sælge til <br>
                            -	Hvem skaber vi værdi for?</p>
                        
                            <h3>Værditilbud</h3>
                            <p>
                            -	Produkter eller ydelser som skaber værdi for kundesegmenterne <br>
                            -	Hvilken værdi skaber vi for kunden?
                            </p>
                        
                            <h3>Kanaler</h3>
                            <p>
                            -	Hvordan virksomheden kommunikerer med og når ud til kundesegmenterne <br>
                            -	Hvordan når vi ud til kunderne?
                            </p>
                                
                            <h3>Kunderelationer</h3>
                            <p>
                            -	Hvilken type relation virksomheden har etableret til kundesegmenterne <br>
                            -	Hvilken type relation forventer vores kundesegmenter?
                            </p>

                            <h3>Indtægtsstrømme</h3>
                            <p>
                            -	De penge som virksomheden genererer  <br>
                            -	Hvad betaler kundesegmenterne for værdien?
                            </p>

                            <h3>Nøgleressourcer</h3>
                            <p>
                            -	De vigtigste aktiver der er nødvendige for at få forretningsmodellen til at virke <br>
                            -	Hvilke nøgleressourcer kræver værditilbuddet?
                            </p>

                            <h3>Nøgleaktiviteter</h3>
                            <p>
                            -	De vigtigste ting virksomheden skal gøre for at få sin forretningsmodel til at virke <br>
                            -	Hvilke nøgleaktiviteter kræver værditilbuddet?
                            </p>

                            <h3>Nøglepartnere</h3>
                            <p>
                            -	Det netværk af partnere og leverandører der får forretningsmodellen til at virke <br>
                            -	Hvem er virksomhedens nøglepartnere? 
                            </p>

                            <h3>Omkostningsstruktur</h3>
                            <p>
                            -	De omkostninger der er forbundet med at drive virksomheden <br>
                            -	Hvad er virksomhedens vigtigste omkostninger?
                            </p>

                            <p>(Osterwalder & Pigneur, 2020)</p>
                        
                                <br>
                        
                            <p>Svarene på de ni byggesten indsættes i et tomt Business Model Canvas, hvorefter det udgør hele virksomhedens forretningsmodel. Den nederste del af modellen handler om virksomhedens økonomi, den højre del om salg, den venstre del om selve virksomheden og den midterste del om produktet (Schmith, Business Model Canvas, 2020).</p>

