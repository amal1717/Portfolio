                            <h2>Design Thinking metoden</h2>

                            <p>Design thinking er en innovationsmetode som bidrager til at identificere de faktiske udfordringer samt brugernes reelle behov og ikke hvad man tror, de har behov for. Metoden hjælper nemlig med at identificere de rigtige spørgsmål, så man ikke går i gang med at udvikle et produkt der ikke løser den faktiske udfordring (Ditmer, 2020). Design thinking metoden består af fem faser, emphathize, define, ideate, prototype og test, som skal følges i samme rækkefølge for at der opnås et optimalt resultat.</p>


                            <h3>1.	Empathize</h3>
                            <p>Denne fase handler om at indsamle information, være nysgerrige og forstå konteksten. Det er vigtigt, at man bruger en del tid i denne fase, da resultatet af denne fase danner baggrund for hele forløbet. Man kan hurtigt komme til at antage hvad udfordringen er, men hvis det ikke er undersøgt tilstrækkeligt, er der blot tale om en antagelse (Hackathon - Fase 1, 2020). Man skal være opmærksom på at løsningsforslag ikke hører til i denne fase, så derfor skal disse blot nedskrives og gemmes til ideate-fasen (Design Thinking projekt - Fase 1 , 2020).</p>


                            <h3>2.	Define</h3>
                            <p>Denne fase handler om at skabe mening ud af alt den viden man fik i empathize-fasen og definerer den helt rigtige problemstilling, som projektet skal tage udgangspunkt i. Det er vigtigt den indsamlede data er visuelt, håndgribeligt og tilgængeligt præsenteret, f.eks. ved at benytte whiteboards eller lignende. (Hackathon - Fase 2 , 2020)</p>


                            <h3>3.	Ideate</h3>
                            <p>Denne fase går ud på at udvikle så mange idéer som muligt til potentielle løsninger. Det er vigtigt, at man fortsætter med at idéudvikle selvom man får en favoritidé, fordi man skal skubbe til forestillingen om hvad der kan lade sig gøre og give plads til mindre oplagte idéer (Hackathon - fase 3, 2020). I denne fase skal man altså ikke være kritisk, men være åben og fordomsfri (Design Thinking projekt - fase 3, 2020).</p>


                           <h3>4.	Prototype</h3>
                            <p>Denne fase handler om at udvikle en fysisk repræsentation af den idé man valgte i ideate-fasen. I starten arbejdes med lavpraktiske og billige prototyper, idet man først skal bruge flere ressourcer på prototyper når man nærmer sig en mere færdig løsning. Prototyping er et godt redskab til at gøre idéen mere håndgribelig og få andre til at forstå idéen bedre (Hackathon - fase 4, 2020).</p>


                            <h3>5.	Test</h3>
                            <p>Denne fase handler om at teste prototypen, helst mange gange, så man opnår troværdigt feedback, som man kan bruge til at forbedre løsningen. Det er vigtigt, at man ikke forsøger at påvirke testpersonerne og samtidig har et åbent sind i forhold til den feedback som de giver (Hackathon - fase 5, 2020).</p>


