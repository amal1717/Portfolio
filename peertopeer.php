                            <h2>Peer-to-peer projektet</h2>
                            <p>Dette projekt handlede om at arbejde kreativt med Photoshop og Illustrator samt om at anvende vores tilegnede viden om typografi, farver, designprincipper osv. Opgaven gik ud på at vi skulle designe en plakat i A4-format ud fra teamet ’Out of frame’ og så skulle der indgå et billede af os selv på plakaten, men udover dette havde vi fri rammer og måtte bruge vores fantasi (Peer-to-peer: Photoshop - Illustrator, 2020). </p>
                    </section>
                    
                    <section id="forløbet">
                            <h2>Forløbet</h2>
                            <p>Jeg startede med at søge efter inspiration på forskellige hjemmesider (pexels.com, 2020) (unsplash.com, 2020). Indenfor kort tid fandt jeg ud af, at jeg ville bruge et landskabsbillede som baggrund og at jeg også skulle bruge en måne på billedet. Jeg havde nemlig fået den idé at jeg skulle stå på månen og fiske efter en ramme. Jeg fandt først landskabsbilledet, men på dette billede var der ingen måne, så derfor valgte jeg at indsætte én. Idet opgavens tema var ’Out of frame’ ledte jeg efterfølgende efter en ramme som passede til landskabsbillede. Jeg fandt en ramme i guld som jeg synes passede rigtig godt, så den satte jeg ind i billedet, hvorefter jeg fik den idé at jeg også skulle hænge ned fra denne ramme, som jeg selv fiskede efter. Både billedet af mig der fisker og billedet af mig der hænger, fik jeg en klassekammerat til at tage, hvorefter jeg klippede mig selv ud af billederne i Photoshop, så jeg kunne bruge dem til plakaten. Efter jeg havde gjort dette, var min første version af billedet færdig, men jeg valgte derefter at opsøge vores lærer for at få feedback, da jeg stadig havde tid til at foretage ændringer. Det betød at jeg gjorde månen og mig selv på månen mindre for at skabe mere symmetri i billedet og så fik jeg min klassekammerat til at tage et nyt billede af mig der fisker, hvor jeg havde fronten mere mod kameraret. Jeg ændrede også fiskestangen så den bøjede ligesom fiskestænger gør, når der er noget på krogen og derefter tilføjede jeg en refleksion af månen i vandet og indsatte billeder af billedet i selve rammen, hvor jeg hænger. Til sidst valgte jeg farven og skrifttypen til teksten, hvorefter Marie hjalp med at finde på selve teksten.</p>
                        </section>
              
                
                                      
