                            <h2>Licensaftaler</h2>
                            <p>En licensaftale er en aftale mellem to parter, hvor licensgiver (indehaver af rettighed) giver licenstager (virksomhed eller privat) tilladelse til at bruge rettigheden mod licensafgift (betaling). Vilkårene i licensaftalen fastlægger hvordan licenstager må råde over rettigheden (Patent- og varemærkestyrelsen, 2020).</p>
                            
                            <h3>jQuery</h3>
                            <p>jQuery fra JavaScript er udgivet under MIT-licensen, som er en simpel licens, hvor den eneste betingelse er at copyright og licensmeddelelsen skal bevares i materialet. Ellers må den ændres og distribueres samt anvendes til både kommerciel og privat brug. Dog inkluderer licensen en begrænsning af ansvaret og giver ikke nogen garanti (MIT License, 2020).</p>
                                <br><br>
                            <img class="billeder" src="Billeder/Ophavsret/mit_license.png" alt="Billede af MIT License">
                            <p>(MIT License, 2020)</p>
                            
                            <h3>Bootstrap</h3>
                            <p>Bootstrap er også udgivet under MIT-licensen og har copyright 2018 Twitter. Betingelsen for at anvende Bootstrap er blot at man skal er at opbevare licens- og copyrightmeddelelsen inkluderet i Bootstraps CSS- og JavaScript-filer, når du bruger dem i dine værker (License FAQs, 2020).</p>
                            
                            <h3>Creative Commons</h3>
                            <p>Creative Commons er en organisation, som tilbyder til række åbne og gratis licenser, som alle kan anvende, hvis man vil offentliggøre et værk. Licenserne gør det muligt for ophavsmanden at give andre tilladelse til at bruge værket, dog under nogle bestemte betingelser. Licenserne giver nemlig ophavsmaden mulighed for at vælge hvordan andre må bruge værket. Derfor skal ophavsmanden vælge den licenseringsform som vedkommende synes virker mest tiltrækkende (Hvad er Creative Commons?, 2020).</p>
                            
