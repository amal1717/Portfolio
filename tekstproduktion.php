                        <section id="nyhedskriterier">
                            <h2>Nyhedskriterier</h2>
                            <p>Nyhedskriterier er de kriterier som en historie skal leve op til for at være en god historie og det er vigtigt at man kender sin målgruppe rigtig godt for at bedømme disse kriterier på deres vegne. En god historie lever nemlig typisk op til alle de nedenstående nyhedskriterier, hvilket gør den mere relevant for målgruppen (Busch A. M., 2015). 
                                <br><br>
                            1.	Væsentlighed<br>
                            o	Er nyheden væsentlig? <br>
                            o	Har den en betydning for mange mennesker?<br><br>
                            2.	Sensation<br>
                            o	Er der tale om en sensation? <br>
                            o	Fascinerer eller chokerer det? <br><br>
                            3.	Konflikt<br>
                            o	Er der gemt en konflikt?<br>
                            o	Er nogle på kollisionskurs?<br><br>
                            4.	Identifikation<br>
                            o	Er det muligt at identificere sig med indholdet?<br>
                            o	Kan læseren genkende sig selv i historien?<br><br>
                            5.	Aktualitet <br>
                            o	Er det aktuelt og vigtig nu og her?<br>
                            o	Er det oppe i tiden? <br>
                                <br>
                            (Busch A. M., 2015) (Schmith, Tekstproduktion, 2020)
                            </p>
                        </section>
                    
                    <section id="budskab">
                            <h2>Budskab</h2>
                            <p>Et budskab består af en påstand samt argumentation for påstanden. En påstand består af få elementer, som kaldes exordium elementer og argumentationen kan bestå af mange elementer, som kaldes corpus elementer. Påstandens elementer er indledning og overskrift, så derfor ses de først og argumentationens elementer er brødteksten, så derfor ses de til sidst (Schmith, Tekstproduktion, 2020).</p>
                    </section>
                        
