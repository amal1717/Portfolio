                        <section id="tekstproduktion">
                            <h2>Tekstproduktion</h2>
                            <p>Tekster er overalt, specielt på internettet, og derfor er det endnu mere vigtigt at man som multimediedesigner lærer at skrive tekster. Det handler om at øvelse gør mester samt at forstå de forskellige mediers kendetegn og hvordan man bruger de forskellige metoder til at producere tekster og skabe en effektiv kommunikation. Tekst kan være med til at skabe købsadfærd eller ændre modtagerens viden, men for at det sker skal man vide hvad man vil med teksten og have en strategi. Derudover skal man også kende til de journalistiske grundbegreber inden man kaster sig ud i tekstproduktion (Busch A. M., 2015).</p>
                        </section>
                    
                    
