                            <p>(Busch A. M., 2015)</p>
                    </section>
                    
                    <section id="webtekster">
                            <h2>Webtekster</h2>
                            <p>Når man skal skrive tekster til internettet, handler det bl.a. om at fange læserens opmærksomhed og have en brugervenlig platform, som gør det let for læseren at skimme indholdet, navigere rundt på siden og træffe beslutninger via call to action. Derfor benytter mange afsendere på nettet først en ”korttekst” og dernæst en ”heltekst”. En ”korttekst” består af en overskrift, et billede og et kort resume og disse kan læseren altså bruge til at danne sig et overblik og beslutte hvilken ”korttekst” der skal klikkes på for at læse mere. En ”heltekst” er derfor den udvidede udgave af ”kortteksten”. Når man skriver tekster til internettet, skal man skrive dem så de kan fungere selvstændigt. Det gør man ved aldrig at antage at læseren har læst andre tekster på sitet. Det kan derfor være en god ide ikke at skrive tekster i en bestemt rækkefølge, men skrive dem i en tilfældig rækkefølge (Busch A. M., 2015).  
                            </p> 
                        
                            <h3>Tips</h3>
                            <p>
                            -	Undgå at skrive ”Se mere her” eller ”Læs mere her”<br>
                            -	Skriv f.eks. ”Læs det sidste nyhedsbrev her” <br>
                            -	Vær opmærksom på hvem målgruppen er <br>
                            -	Overvej målet og hvordan teksten målrettes målgruppen<br>
                            -	Brug aldrig lange og indviklede sætninger <br>
                            -	Vær opmærksom på sidens formål<br>
                            -	Overvej muligheder for eksterne links <br>
                            -	Obs. Målgruppens informationskrav<br>
                            -	Overvej kobling til de sociale medier <br><br>
                                
                            (Busch A. M., 2015)
                            </p>
                    </section>
                    
                     <section id="socialemedietekster">
                            <h2>Sociale medietekster</h2>
                            <p>Nedenstående model kan bidrage til at tænke det strategiske ind i den skriftlige kommunikation på de sociale medier. Dog kan modellen også bruges til traditionelle medier. Hver farve står for en bestemt strategi som man kan bruge til at håndtere kommunikationen mellem en virksomhed og kunderne f.eks. når en virksomhed besvarer kommentarer på Facebook (Busch A. M., 2015).</p>
                        
