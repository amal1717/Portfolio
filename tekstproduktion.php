                            <h3>7 geniale genveje</h3>
                            <p>
                                1.	Lav en god overskrift <br>
                                2.	Lav et kort resume på et par linjer <br>
                                3.	Lav afsnit med underoverskrifter <br>
                                4.	Skriv kun om et emne i hvert afsnit <br>
                                5.	Fremhæv vigtige nøgleord <br>
                                6.	Brug punkter hvor det giver mening<br>
                                7.	Henvis til meningsfulde links <br>
                                <br>
                                (Schmith, Tekstproduktion, 2020)
                            </p>
