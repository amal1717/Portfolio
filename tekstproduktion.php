                    <section id="klaretekster">
                        <h2>Gode råd til at skrive klare tekster</h2>
                        <p>
                            -	Skriv aktivt og via at der er et menneske bag teksten<br>
                            -	Skriv konkret og undgå at være for abstrakt<br>
                            -	Skriv kort både hvad angår tekster, sætninger og ord<br>
                            -	Skriv, som du taler og lige ud af landevejen<br><br>
                            
                            (Friis, 2015)
                        </p>
