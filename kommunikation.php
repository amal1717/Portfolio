                        </section>
                    
                        <section id="kotlers">
                            <h3>Kotlers model for markedskommunikation</h3>
                            <p>Denne model bygger videre på Lasswells formel, da den både beskriver en forbindelse fra afsender til modtager og fra modtager til afsender. Modellen beskriver hvordan afsender koder og sender et budskab, som modtageren skal afkode for at forstå. Derefter reagerer modtager på budskabet og giver en respons, som påvirkes af støj fra omgivelserne og derefter bliver responsen til feedback som afsenderen kan tolke (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015).</p>
                            
