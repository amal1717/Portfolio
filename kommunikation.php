                        <section id="lasswells">
                            <h3>Lasswells formel</h3>
                            <p>Lasswells formel kan bruges til at beskrive en kommunikationshandling ved at besvare de spørgsmål som modellen består af, nemlig ”Hvem siger hvad i hvilken kanal til hvem med hvilken effekt?”. Denne model medtænker dermed mediet og tillægger det betydning hvilket medie er anvendes. Der fokuseres hovedsagelig på afsenderen, men der er også lidt fokus på modtageren, idet modtageren identificeres (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015).</p>
                            
                                <br>
                            
