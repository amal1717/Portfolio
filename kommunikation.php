                        <section id="kommunikation">
                            <h2>Grundlæggende kommunikation</h2>
                            <p>Man kan dele kommunikationsteorier op i to paradigmer, som er to forskellige måder at se verdenen på og dermed to forskellige måder at se kommunikation på. Et paradigme er altså en måde at forstå og forklare verden på. I forhold til kommunikation er der fokus på det humanistiske paradigme og det samfundsvidenskabelige paradigme. Paradigmerne kobler sig til to forskellige videnskabelige områder, som hver især har nogle metoder og teorier, fordi paradigmerne forsøger at begribe noget forskelligt (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                        </section>
                    
                        <section id="humanistiske">
                            <h2>Den humanistiske paradigme</h2>
                            <p>Indenfor det humanistiske paradigme er fokus på modtageren, der ses som medskabende og aktiv. Modtageren er ”medbestemmer” af budskabet, fordi det er modtageren der har ansvaret for afkodningen af budskabet og derfor er budskabet og modtagerens forståelse af budskabet centralt i dette paradigme (Schmith, Grundlæggende kommunikation, 2020). Paradigmet kaldes også for interaktionsparadigmet, fordi der er fokus på interaktion og dialogen mellem mennesker. Derudover kommer de fleste metoder og teorier i det humanistiske paradigme primært fra humanistiske områder, som psykologi, sprog, kultur osv. Overordnet set fokuserer dette paradigmes kommunikationsmodeller på hele kommunikationssituationen og ligestiller afsender og modtager. Hvis der ikke er en modtager til at fortolke budskabet, eksisterer budskabet i kommunikativ forstand ikke (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                        </section>
                    
                    
                        <section id="imkmodellen">
                            <h3>IMK-modellen</h3>
                            <p>IMK står for International Markeds Kommunikation og er en model der sætter ”teksten” i fokus som det afsenderen og modtageren producerer i fællesskab, hvilket gør dem ligeværdige i kommunikationen. Det betyder at der ikke eksisterer nogen kommunikation før modtager deltager, hvilket ses som et modsvar til Kotlers model for markedskommunikation. Modellen giver også udtryk for at konteksten, kulturen, medier og genre er med til at forme ”testen” (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                            
