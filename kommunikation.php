                        <section id="samfundsvidenskabelige">
                            <h2>Det samfundsvidenskabelige paradigme</h2>
                            <p>Det samfundsvidenskabelige paradigme har fokus på afsenderen og præges af transmission. Det betyder at der ikke er fokus på interaktion og på modtageren i kommunikationen, som i det humanistiske paradigme, men at der fokuseres på envejskommunikation og derfor ses afsenderen som den styrende i kommunikationen. Derudover kommer de fleste metoder og teorier i det samfundsvidenskabelige paradigme primært fra samfundsvidenskabelige områder, som politik, økonomi, jura osv. Dette paradigme er overvejende karakteriseret ved objektivitet samt at troen på verdenen er forudsigelig og at man kan planlægge med udgangspunkt i stabile omgivelser (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                        </section>
                    
                    
                        <section id="kanyleteorien">
                            <h3>Kanyleteorien</h3>
                            <p>Kanyleteorien handler om at man metaforisk set kan sprøjte ideer, tanker og holdninger ind i modtagerne ved hjælp af propaganda og på den måde kontrollere samfundet og derfor bygger teorien på at medierne kan påvirke os som mennesker (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                        </section>
                    
                    
                    
                        <section id="sormodellen">
                            <h3>S-O-R-modellen</h3>
                            <p>S-O-R-modellen angiver at mennesker styres via påvirkning og stimuli (S) og at kommunikationen derefter kanaliseres gennem en organisme (O) og ikke overføres direkte til modtagerne. Mennesker ses derfor ikke som værende ens, men som individuelle mennesker hvor responsen ikke vil være ens og hvor der skal tages forskellige hensyn til forskellige grupper. Herefter følger en respons (R) på det budskab som blev sendt og jo kraftigere budskabet er, desto større vil responsen være (Busch A. , Kapitel 5 - Grundlæggende kommunikationsteori, 2015). </p>
                            
