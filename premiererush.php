                        <h2>Quick'n Dirty projekt</h2>
                        
                        <p>For at lære Premiere Rush at kende skulle vi i grupper producere en informationsvideo som enten skulle promovere eller øge kendskabet til et produkt eller en service. Vi skulle tage udgangspunkt i et produkt eller en service som vi i forvejen kendte til og så skulle videoen vare 1 ½ - 2 ½ minutter og indeholde forskellige kameravinkler. Først skulle vi bruge 2 ½ time på pre-production, som handler om mål, formål, målgruppe, genre, storyboard og shot-list. Herefter skulle vi bruge 1 ½ time på production, som handler om optagelsen af de forskellige klip og til dette skulle vi blot bruge en smartphone og naturligt lys. Til sidst havde vi 1 time til post-production, som handler om redigering af videoen, inden vi skulle fremlægge resultatet for klassen (Bogner, Quick'n Dirty Video, 2020).</p>
                        
