                        <section id="tools">
                            <h2>Værktøjer</h2>
                            <p>Inden man kan lave en video eller lignende i Premiere Rush skal man først og fremmest sørge for de optagelser som skal bruges til videoen. Derefter åbner man programmet, vælger mappen med optagelserne og vælger de optagelser som skal bruges til videoen i den rækkefølge de skal bruges. Når man har gjort dette, hvilket ses på nedenstående billede, trykker man på ”Create” knappen nede i højre hjørne, hvorefter programmet sætter optagelserne sammen til en lang video. Dernæst kan man begynde at klippe de forskellige optagelser ved hjælp af værktøjerne nede i venstre hjørne.</p>
                            <img class="billeder" src="Billeder/Premiere_Rush/valg_af_klip.png" alt="Valg af forskellige klip i Premiere Rush">
                       
                                <br><br><br>
                            
                            <p>For at klippe i videoen flytter man den blå streg derhen hvor optagelsen skal klippes over og klikker på saksen. Derefter flytter man stregen til højre eller til venstre afhængig af hvilket klip der skal slettes og trykker på skraldespanden. Man kan også blot højreklikke på det klip der skal slettes og trykke på skraldespanden.</p>
                            <img class="billeder" src="Billeder/Premiere_Rush/klip_og_slet.png" alt="Klip og slet i Premiere Rush">
                            
                                <br><br><br>
                            
                            <p>Man kan duplikere en optagelse ved at markere optagelsen og derefter trykke på plusikonet mellem saksen og skraldespanden. Så indsættes den duplikerede optagelse lige ved siden af.</p>
                            <img class="billeder" src="Billeder/Premiere_Rush/duplikere.png" alt="Duplikere i Premiere Rush">
                            
                                <br><br><br>
                            
                            <p>Hvis man klikker på plusikonet oppe i venstre hjørne, har man bl.a. mulighed for at indsætte nye optagelser, billeder, grafik, tekst, musik, lydeffekter og voiceover.</p>
                            <img class="billeder" src="Billeder/Premiere_Rush/forskellige_tools_premiere_rush1.png" alt="Værktøjer i Premiere Rush">
                           
                                <br><br><br>
                            
                            <p>Oppe i højre hjørne af programmet har man også mulighed for at indsætte grafik og lyd, men derudover kan man også indsætte grafik og effekter, ændre på farverne og hastigheden samt beskære og rotere videoen.</p>
                            <img class="billeder" src="Billeder/Premiere_Rush/forskellige_tools_premiere_rush2.png" alt="Værktøjer i Premiere Rush">
                    
                                <br><br><br>
                            
                            <p>Under ”Share” kan man vælge hvor man vil gemme videoen, men man kan også vælge hvordan man vil dele den f.eks. på Youtube, Facebook eller Instagram. </p>
