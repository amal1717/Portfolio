                        <section id="premiererush">
                            <h2>Premiere Rush</h2>
                            <p>Premiere Rush blev lanceret i 2018 og er et videoprogram som er udviklet af Adobe. Programmet er et moderne alt-i-ét program som bruges til at producere forholdsvis simple videoer og film samt levende grafik til digitale og sociale medier. En af fordelene ved Premiere Rush er bl.a. at man kan importere professionelle skabeloner som f.eks. overgange, effekter, titler og lyde, som man kan bruge i sine produktioner. Oftest vil man installere og anvende programmet på sin computer, men Premiere Rush findes også som en app og derfor kan man også vælge at optage videoer på sin mobil og redigere videoen direkte i appen  (Hvad kan Adobe Premiere Rush?, 2019). 
                                <br><br>
                            Premiere Rush er en ”light” udgave af programmet Premiere Pro, som er et mere avanceret videoprogram. Det betyder at Premiere Rush kun indeholder de mest nødvendige funktioner, så hvis man ønsker at arbejde med tunge videoproduktioner og avancerede funktioner, er det derfor bedst at bruge Premiere Pro. Idet Premiere Rush er udviklet af Adobe ligner brugerfladen, panelerne og værktøjerne dem man kender fra bl.a. Photoshop og Illustrator. Det tager derfor ikke lang tid at lære Premiere Rush at kende, hvis man forvejen er bekendt med Adobes andre programmer (Hvad kan Adobe Premiere Rush?, 2019). 
                            </p>
                      
