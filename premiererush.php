                        <h2 id="refleksion">Refleksion</h2>
                        <p>Premiere Rush er et simpelt og hurtigt program, så da vi skulle anvende programmet til vores Quick’n Dirty projekt, besluttede vi at prøve os frem. Dog havde vi inden undervisningen set en 15 minutters video om hvordan man bruger programmet, hvilket gjorde at vi ikke var helt på bar bund. Det tog heldigvis ikke lang tid at lære programmet at kende, idet vi kun havde 1 time til at redigere videoen inden fremlæggelserne. Det var ikke mig der sad med computeren da vi redigerede videoen, fordi vi var seks personer i gruppen. Dog sad jeg lige ved siden af og fulgte med, imens vi snakkede om hvordan vi skulle klippe videoen sammen. Dette fungerede helt fint og jeg lærte også meget ved det, men alligevel havde jeg brug for at sidde med programmet på egen hånd og klippe inden jeg skulle lave denne side.</p>
