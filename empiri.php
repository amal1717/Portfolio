                        <h3>Fordele og ulemper</h3>    
                        <p>Hvis man ønsker at foretage en statistik, kan man med fordel anvende kvantitative metoder, idet disse metoder giver god mulighed for at indsamle tal, som kan bruges til dette (Busch, 2015). Kvantitative metoder er knap så dybdegående og tidskrævende som kvalitative metoder, og derfor er disse heller ikke lige så bekostelige som de kvalitative metoder (Kvantitativ metode, 2020).
                        <br><br>
                        Ulemper ved brugen af kvantitative metoder er bl.a. at der er begrænset mulighed for at opnå uddybende data, fordi der ofte anvendes simple spørgsmål. Samtidig varierer mængden af information fra hver respondent og man opnår generelt færre informationer pr. respondent (Andersen, Jensen, Olsen, Olsen, & Schmalz, Kapitel 4 , 2015). 
                        <br><br>
                        Derudover kan det være svært at tolke på spørgeskemaerne efterfølgende, fordi vi mennesker forstår og besvarer spørgsmålene ud fra vores egen individuelle opfattelse (Busch, 2015).<br>
                        Observation kan være en dyr metode, hvis den observerede adfærd forekommer sjældent, men det kan også være en billig metode, hvis adfærden forekommer ofte. Eksperimenter er en relativt dyr og tidskrævende metode, men på trods af det kan den i nogle tilfælde være en god og indbringende metode (Andersen, Jensen, Olsen, Olsen, & Schmalz, Kapitel 6, 2015). </p>    

