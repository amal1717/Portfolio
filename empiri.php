                            <h2>Kvantitativ og kvalitativ empiri</h2>
                            <p>Kvantitativ og kvalitativ empiri er to forskellige metoder, som bruges til at indsamle forskellige typer data. Man vælger derfor hvilken metode man vil anvende afhængig af hvad der skal undersøges, men det kan også være en fordel at anvende begge metoder for at gøre undersøgelsen mere dybdegående og opnå mere præcis data. 
                            </p>
